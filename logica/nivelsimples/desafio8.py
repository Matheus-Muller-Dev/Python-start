# Verificando a o maximo e o minimo de uma lista de números
# usando as funções nativas do python max e min

numeroMaior = 100
numeroMenor = 10
# Lista com todos os números
numeros = [numeroMaior, numeroMenor]

# Funções nativas do python
numero_max = max(numeros)
numero_min = min(numeros)

print("O salário máximo é: ", numero_max)
print("O salário mínimo é: ", numero_min)