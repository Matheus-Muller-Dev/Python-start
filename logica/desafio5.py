# Verificando a média de três números
print("Vamos verificar a media de três numeros")

numero = float(input("Qual o primeiro número: "))
numero2 = float(input("Qual o segundo número: "))
numero3 = float(input("Qual o tercero número: "))

total = (numero + numero2 + numero3) / 2

print(f"A media dos números {total}")